int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < 'A' || 'Z' < str[i]) && (str[i] < 'a' || 'z' < str[i]))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
